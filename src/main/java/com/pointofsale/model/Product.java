package com.pointofsale.model;

public class Product {
    private String name;
    private int value;
    private int id;
    private String linkImage;

    public String getName() {
        return name;
    }

    public int getValue() {
        return value;
    }

    public int getId() {
        return id;
    }

    public String getLinkImage() {
        return linkImage;
    }
}
